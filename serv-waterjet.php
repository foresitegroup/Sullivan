Water jet cutting is one of the most versatile cutting processes around. Well suited for high-performance metal cutting, water jet technology, when used with an abrasive additive, creates a clean, burr-free cut that does not require secondary finishing and does not leave a heat-affected zone. Nearly every type of cut is possible, from rapid hole drilling to detailed cuts for automotive applications.<br>
<br>
The benefits of using Sullivan Water jet services include:
<ul>
  <li>No thickness limitations; up to 6".</li>
  <li>Improved accuracy and finish over flame cutting and plasma cutting</li>
  <li>No issues with reflective materials such as brass, copper, and aluminum.</li>
  <li>No heat input, so there is no heat-affected zone</li>
  <li>Hardened materials can be cut with no change in temper</li>
</ul>

