<strong>Abrasive blasting</strong> is the operation of cleaning or preparing a surface by forcibly propelling a stream of abrasive material against it under high pressure. Usually explained as the use of a material against another material to make it smoother, remove surface contaminants or to roughen a surface. There are several variants of the process, such as bead blasting, sandblasting, shot blasting.<br>
